# Ejercicio 3. Determinar la edad de una persona conociendo el año actual y el año de nacimiento.

# Importar librerias o bibliotecas

# ENTRADA DE DATOS
# Declarar, crear o instanciar variables
año_nacimiento = int(input("Ingresa tu año de nacimiento: "))

año_actual = 2024

# PROCESOS (Operaciones y/o calculos matemáticos y lógicos)
edad = año_actual - año_nacimiento

# SALIDA DE DATOS
print("La edad es: ", edad)

